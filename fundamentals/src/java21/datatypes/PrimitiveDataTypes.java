package java21.datatypes;

public class PrimitiveDataTypes {

    public static void main(String[] args) {

        System.out.println("Default Values of Primitive Data Types in Java:");
        System.out.println("byteMinValue: " + Byte.MIN_VALUE);
        System.out.println("byteMaxValue: " + Byte.MAX_VALUE);
        System.out.println();
        System.out.println("shortMinValue: " + Short.MIN_VALUE);
        System.out.println("shortMaxValue: " + Short.MAX_VALUE);
        System.out.println();
        System.out.println("intMinValue: " + Integer.MIN_VALUE);
        System.out.println("intMaxValue: " + Integer.MAX_VALUE);
        System.out.println();
        System.out.println("longMinValue: " + Long.MIN_VALUE);
        System.out.println("longMaxValue: " + Long.MAX_VALUE);
        System.out.println();
        System.out.println("floatMinValue: " + Float.MIN_VALUE);
        System.out.println("floatMaxValue: " + Float.MAX_VALUE);
        System.out.println();
        System.out.println("doubleMinValue: " + Double.MIN_VALUE);
        System.out.println("doubleMaxValue: " + Double.MAX_VALUE);
        System.out.println();
        System.out.println("charMinValue: " + Character.MIN_VALUE);
        System.out.println("charMaxValue: " + Character.MAX_VALUE);
        System.out.println();
        System.out.println("booleanValues: " + Boolean.FALSE + ", " + Boolean.TRUE);
    }
}
